/**
 * Fuzzy city resolver. Handles aliases, exact matches, state abbreviation
 * expansion, and city-name-only prefix matching.
 */
export function resolveCity(input: string): { lat: number; lng: number } | null {
  const normalized = input.trim().toLowerCase();

  if (!normalized) return null;

  // 1. Check aliases
  const aliasKey = ALIASES[normalized];
  if (aliasKey) {
    return cityLookupMap.get(aliasKey) ?? null;
  }

  // 2. Exact match
  const exact = cityLookupMap.get(normalized);
  if (exact) return exact;

  // 3. If input contains a comma, try expanding the state abbreviation
  if (normalized.includes(',')) {
    const [cityPart, statePart] = normalized.split(',').map((s) => s.trim());
    const fullState = STATE_ABBREVIATIONS[statePart];
    if (fullState) {
      const expanded = `${cityPart}, ${fullState}`;
      const match = cityLookupMap.get(expanded);
      if (match) return match;
    }
  }

  // 4. No comma — try matching city name only (first match where key starts with "input,")
  if (!normalized.includes(',')) {
    const prefix = `${normalized},`;
    for (const [key, value] of cityLookupMap) {
      if (key.startsWith(prefix)) {
        return value;
      }
    }
  }

  return null;
}

/**
 * Returns all city names from the lookup map formatted in Title Case.
 * Result is cached after first computation.
 */
let cachedCityNames: string[] | null = null;

/** Words that should stay lowercase in title case (unless first word) */
const LOWERCASE_WORDS = new Set(['by', 'the', 'of', 'and', 'in', 'at', 'to', 'for', 'on', 'a', 'an']);

function toTitleCase(str: string): string {
  return str.replace(/\b\w+/g, (word, index) => {
    if (index > 0 && LOWERCASE_WORDS.has(word.toLowerCase())) {
      return word.toLowerCase();
    }
    return word.charAt(0).toUpperCase() + word.slice(1);
  });
}

export function getCityNames(): string[] {
  if (cachedCityNames) return cachedCityNames;

  cachedCityNames = Array.from(cityLookupMap.keys()).map((key) => {
    // Split on ", " to handle city and state separately
    const parts = key.split(', ');
    return parts.map((part) => toTitleCase(part)).join(', ');
  });

  return cachedCityNames;
}
